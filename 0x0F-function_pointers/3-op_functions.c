/**
 * op_add - sum
 * @a: integer parameter
 * @b: integer parameter
 * Return: integer value
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract
 * @a: integer parameter
 * @b: integer parameter
 *
 * Return: integer value
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply
 * @a: integer parameter
 * @b: integer parameter
 *
 * Return: integer value
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide
 * @a: integer parameter
 * @b: integer parameter
 *
 * Return: integer  valu
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module
 * @a: integer parameter
 * @b: integer parameter
 *
 * Return: integer value
 */
int op_mod(int a, int b)
{
	return (a % b);
}
